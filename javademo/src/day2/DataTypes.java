package day2;

public class DataTypes {

	public static void main(String[] args) 
	{
		int values=9/2;
		float value1=101/61;
		double value2=10d/6d;
		float value3=5/2.5f;
		System.out.println("Value1="+values);
		System.out.println("values2="+value1);
		System.out.println("values3="+value2);
		System.out.println("Values4="+value3);
		
		int marker=512;
		
		double percentage= marker*0.46f;
		System.out.println("percentage:"+percentage);
		

	}

}
